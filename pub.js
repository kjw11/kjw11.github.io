var filter = function (year) {
    var final = [];
    for (var i = 0; i < publications.length; i++) {
        if (publications[i].time == year) {
            final.push(publications[i]);
        }
    }
    return final;
};


var outputHtml = function (publicationsArr) {

    // if empty
    if (publicationsArr.length == 0) {
        return [];
    }

    var html = [];
    html.push('<ul>');
    for (var i = 0; i < publicationsArr.length; i++) {

        html.push('<li>');
        html.push('<p>');
        html.push('<a href="' + publicationsArr[i].url + '" target="_blank">' + publicationsArr[i].title + '</a>');
        html.push('</p>');

        // loop authors
        html.push('<p>');
        var auther = [];
        for (var j = 0; j < publicationsArr[i].authors.length; j++) {
            if (publicationsArr[i].authors[j] == 'Jiawen Kang') {
                auther.push('<u>Jiawen Kang</u>');
                // auther.push('Jiawen Kang');
            } else if (publicationsArr[i].authors[j] == 'Jiawen Kang*') {
               auther.push('<u>Jiawen Kang*</u>');
            //    auther.push('Jiawen Kang*');
               var presenter = true
            } else if (publicationsArr[i].authors[j] == 'Jiawen Kang^') {
                auther.push('<u>Jiawen Kang^</u>');
                var equal_contributor = true
            } else {
                auther.push(publicationsArr[i].authors[j]);
            }
        }

        html.push(auther.join(', '));
        if (presenter == true){
            html.push('. (*: Presenter)');
        } else if (equal_contributor == true){
            html.push('. (^: Equal Contribution)');
        } else {
            html.push('.');
        }
        html.push('</p>');

        html.push('<p>');
        html.push(publicationsArr[i].pub);
        if (publicationsArr[i].highlight != '') {
            html.push(' <b>(' + publicationsArr[i].highlight + ')</b>');
        }
        html.push('</p>');

        html.push('</li>');
    }
    html.push('</ul>');
    return html;
};


