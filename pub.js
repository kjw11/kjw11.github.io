var filter = function (year) {
    var final = [];
    for (var i = 0; i < publications.length; i++) {
        if (publications[i].time == year) {
            final.push(publications[i]);
        }
    }
    return final;
};
var outputHtml = function (publicationsArr) {
    var html = [];
    for (var i = 0; i < publicationsArr.length; i++) {
        html.push('<ul class="pub_ul"><li><b><a href="' + publicationsArr[i].url + '" target="_blank">' + publicationsArr[i].title + '</a></b>');
        html.push('<br>');
        var auther = [];
        for (var j = 0; j < publicationsArr[i].authers.length; j++) {
            if (publicationsArr[i].authers[j] == 'Jiawen Kang') {
                // auther.push('<b>Jiawen Kang</b>');
                auther.push('Jiawen Kang');
            } else if (publicationsArr[i].authers[j] == 'Jiawen Kang*') {
               // auther.push('<b>Jiawen Kang*</b>');
               auther.push('Jiawen Kang*');
            } else {
                auther.push(publicationsArr[i].authers[j]);
            }
        }
        html.push(auther.join(', '));
        html.push('.</li>');
        // html.push(publicationsArr[i].pub + publicationsArr[i].time);
        html.push(publicationsArr[i].pub);
        html.push('</ul>');
    }
    var container = document.getElementById('publications');
    container.innerHTML = html.join('');
};
var initPub = function (year) {
    var publicationsArr = filter(year);
    outputHtml(publicationsArr);
};
var selYear = function () {
    var selector = document.getElementById('select-year');
    var year = selector.value;
    initPub(year);
};
window.onload = function () {
    var year = new Date().getFullYear();
    initPub(year);  

};
